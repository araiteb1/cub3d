void draw_mini_map(void *mlx, void *win)
{
    int x, y;
    int cell_color;

    for (x = 0; x <= MINI_MAP_WIDTH / GRID_SIZE; x++)
    {
        for (y = 0; y <= MINI_MAP_HEIGHT / GRID_SIZE; y++)
        {
            if (worldMap[x][y] == 1)
                cell_color = 0x00ff00;  // Wall
            else if (worldMap[x][y] == 2)
                cell_color = 0xFF0000;  // Player
            else
                cell_color = 0xFFFFFF;  // Empty

            int px, py;
            for (px = 0; px < GRID_SIZE; px++)
            {
                for (py = 0; py < GRID_SIZE; py++)
                {
                    mlx_pixel_put(mlx, win, x * GRID_SIZE  + px, y * GRID_SIZE + py, cell_color);
                }
            }
        }
    }
}

int main(void)
{
    void *mlx;
    void *win;

    mlx = mlx_init();
    win = mlx_new_window(mlx, WIN_WIDTH, WIN_HEIGHT, " Example");
    draw_mini_map(mlx, win);

    mlx_loop(mlx);

    return (0);
}
